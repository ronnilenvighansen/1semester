/* Write a program called Rhombus that displays the following output:
   $$$
  $   $
 $     $
"-'-"-'-"
 $     $
  $   $
   $$$ */
   
public class Rhombus
{
   public static void main(String[] args)
   {
      System.out.println("   $$$   ");
      System.out.println("  $   $  ");
      System.out.println(" $     $ ");
      System.out.println("\"-'-\"-'-\"");
      System.out.println(" $     $ ");
      System.out.println("  $   $  ");
      System.out.println("   $$$   ");
   }
}