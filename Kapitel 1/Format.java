/* Write a complete Java program called Format that prints the following output:
What is the difference between
"\t" and "\n"? 

One is what we see as a tab space on the console.
The other is a new line feed which appears on the console. */

public class Format
{
   public static void main(String[] args)
   {
      System.out.print("What is the difference between \n\"\\t\" \"\\n\"?");
      System.out.println();
      System.out.println();
      System.out.println("One is what we see as a tab space on the console.");
      System.out.println("The other is a new line feed which appears on the console.");
   }
}